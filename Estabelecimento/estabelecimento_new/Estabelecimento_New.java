    public void setarNoJPanelCepSelecionado( Cep Cep ) {                                                  
        try{

            CepX = Cep;
            
                
            MaskFormatter mascara = new MaskFormatter("##.###-###");
            mascara.setValidCharacters("0123456789");                
            tfCep.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory( mascara ) ); 
            
            String cep = Cep.getCep();
            cep = cep.replaceAll(Pattern.compile("\\s").toString(), "");
            cep = cep.replaceAll(Pattern.compile("\\D").toString(), "");
            
            tfCep.setText        ( cep                  );
            tfLogradouro.setText ( Cep.getLogradouro()  );
            tfComplemento.setText( Cep.getComplemento() );
            tfBairro.setText     ( Cep.getBairro()      );
            
            estabelecimento.Estabelecimento Estabelecimento_do_Cep = new estabelecimento.Estabelecimento();
            Estabelecimento_do_Cep.setPais  ( Cep.getIdPais()      );
            Estabelecimento_do_Cep.setEstado( Cep.getIdUf()        );
            Estabelecimento_do_Cep.setCidade( Cep.getIdMunicipio() );
            
            Inicio.setarCBPais     ( true, Estabelecimento_do_Cep ); 
            Inicio.setarCBUf       ( true, Estabelecimento_do_Cep );
            //System.out.println( "Cep.getIdMunicipio() - " + Estabelecimento_do_Cep.getCidade() );
            Inicio.setarCBMunicipio( true, Estabelecimento_do_Cep );
                        
        } catch( Exception e ){ JOPM JOptionPaneMod = new JOPM( 2, "setarNoJPanelCepSelecionado(), \n"
                + e.getMessage() + "\n", this.getClass().getSimpleName() ); }
    }
    
